export function translate(key: string, language: Language, vars?: TemplateVars): string {
  const base = TRANSLATIONS[language] || TRANSLATIONS.vi;
  let text = base[key] ?? TRANSLATIONS.vi[key] ?? key;

  if (vars) {
    Object.entries(vars).forEach(([name, value]) => {
      text = text.replace(new RegExp(`\\{${name}\\}`, "g"), String(value));
    });
  }

  return text;
}
